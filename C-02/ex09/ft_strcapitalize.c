char	*ft_strcapitalize(char *str)
{
 	str[0] = *str - 32;
	str++;
  
    while (*str)
    {
        if (*str == ' ' || *str == '+' || *str == '-')
        {
            str[0] = *str - 32;
        }
        str++;
    }
    return (str);
}
